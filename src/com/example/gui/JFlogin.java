    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        
        String clave = GestorClaves.convertirClave(jPClave.getPassword());
        if(jTUsuario.getText().equals("") || clave.equals("")){
            JOptionPane.showMessageDialog(this, "Olvid칩 ingresar usuario y/o clave!", "Validaci칩n", JOptionPane.ERROR_MESSAGE);
        }else{
            Usuario user = gestor.validarUsuario(jTUsuario.getText(), clave);
            if (user != null) {
                this.dispose();
                new JFAppStore(user).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Usuario y/o clave incorrectos!", "Validaci칩n", JOptionPane.ERROR_MESSAGE);
                jTUsuario.setText("");
                jPClave.setText("");
                jTUsuario.requestFocus();
            }
        }
    }//GEN-LAST:event_jBAceptarActionPerformed

